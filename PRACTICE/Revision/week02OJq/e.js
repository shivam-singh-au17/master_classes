
var Grade = "C";
switch (Grade) {
    case "A":
        console.log("Excellent");
        break;
    case "B":
        console.log("Very Good");
        break;
    case "C":
        console.log("Good");
        break;
    case "D":
        console.log("Needs a lot of Improvement");
        break;
    case "F":
        console.log("Failed");
        break;
    default:
        console.log("Wrong Input");
        break;
}